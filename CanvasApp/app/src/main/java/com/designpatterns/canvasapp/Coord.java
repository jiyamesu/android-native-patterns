package com.designpatterns.canvasapp;

public class Coord {
    private float x;
    private float y;

    public Coord() { }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
